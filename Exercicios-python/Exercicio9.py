# Objetivo: Receber 2 números inteiros. Calcular e mostrar a soma dos quadrados.
num1 = int(input("Informe o valor do primeiro número"));
num2 = int(input("Informe o valor do segundo número"));
num1 = num1**2;
num2 = num2**2;
print("Esse é o valor de num1 ao quadrado" + str(num1));
print("Esse é o valor de num2 ao quadrado" + str(num2));